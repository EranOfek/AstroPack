
# Log levels, used with MsgLogger class, msgLog(), etc.
class LogLevel:
    Non = 0          # Not set
    Error = 1        # Error
    Warning = 2      # Warning
    Info = 3         # General info
    Verbose = 4      # Verbose
    Debug = 5        # Detailed debug
    Test = 6         # Unit-Test
    All = 9          # All

