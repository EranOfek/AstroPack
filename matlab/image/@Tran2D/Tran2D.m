            % Package: @Tran2D (Static)
            % Input  : - Transformation name. Avialable transformations
            %            are:
            %            'cheby1_3_c1' - Fitsr kind Chebyshev polynomials
            %                   of the 3rd degree + color term of the first degree.
            %            'cheby1_3' -  Fitsr kind Chebyshev polynomials
            %                   of the 3rd degree.
            %            'cheby1_4' -  Fitsr kind Chebyshev polynomials
            %                   of the 4th degree.
            %            'cheby1_4_c1' - Fitsr kind Chebyshev polynomials
            %                   of the 4th degre + color term of the first degree.
            %            'cheby1_2' -  Fitsr kind Chebyshev polynomials
            %                   of the 2nd degree.
            %            'poly1' - 1st deg polynomials.
            %            'poly2' - 2nd deg polynomials.
            %            'poly3' - 3rd deg polynomials.
            %            'poly1_tiptilt' - 1st deg polynomials + 2 tip/tilt
            %                   terms.
            % Output : - A cell array of functionals for X coordinates.
            %          - A cell array of functionals for Y coordinates.
            %          - A cell array of argume names.
            %            By default this is {'x','y','c','AM','PA'}.
            %          - For the X functional, vector of degree of X polynomial for each
            %            functional. This is available only for simple
            %            polynomials, otherwise empty.
            %          - VFor the X functional, vector of degree of Y polynomial for each
            %            functional. This is available only for simple
            %            polynomials, otherwise empty.
            %          - For the Y functional, vector of degree of X polynomial for each
            %            functional. This is available only for simple
            %            polynomials, otherwise empty.
            %          - VFor the Y functional, vector of degree of Y polynomial for each
            %            functional. This is available only for simple
            %            polynomials, otherwise empty.
            % Example:
            % [FunX,FunY,ColCell]=Tran2D.selected_trans('cheby1_3_c1')
            
            PolyX_Xdeg = [];
            PolyX_Ydeg = [];
            PolyY_Xdeg = [];
            PolyY_Ydeg = [];
            switch lower(Name)
                case 'cheby1_3_c1'
                    % chebyshev polynomials of the first kind, of order 3 +
                    % color terms of the first order
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) 2.*x.^2-1,...
                                      @(x,y,c,AM,PA) 2.*y.^2-1,...
                                      @(x,y,c,AM,PA) x.*y,...
                                      @(x,y,c,AM,PA) 4.*x.^3 - 3.*x,...
                                      @(x,y,c,AM,PA) 4.*y.^3 - 3.*y,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*y,...
                                      @(x,y,c,AM,PA) (2.*y.^2-1).*x,...
                                      @(x,y,c,AM,PA) c,...
                                      @(x,y,c,AM,PA) x.*c,...
                                      @(x,y,c,AM,PA) y.*c};
                    FunY        = FunX;
                   
                case 'cheby1_3'
                    % chebyshev polynomials of the first kind, of order 3
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) 2.*x.^2-1,...
                                      @(x,y,c,AM,PA) 2.*y.^2-1,...
                                      @(x,y,c,AM,PA) x.*y,...
                                      @(x,y,c,AM,PA) 4.*x.^3 - 3.*x,...
                                      @(x,y,c,AM,PA) 4.*y.^3 - 3.*y,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*y,...
                                      @(x,y,c,AM,PA) (2.*y.^2-1).*x};
                    FunY        = FunX;
                case 'cheby1_4'
                    % chebyshev polynomials of the first kind, of order 4
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) 2.*x.^2-1,...
                                      @(x,y,c,AM,PA) 2.*y.^2-1,...
                                      @(x,y,c,AM,PA) x.*y,...
                                      @(x,y,c,AM,PA) 4.*x.^3 - 3.*x,...
                                      @(x,y,c,AM,PA) 4.*y.^3 - 3.*y,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*y,...
                                      @(x,y,c,AM,PA) (2.*y.^2-1).*x,...
                                      @(x,y,c,AM,PA) (8.*x.^4 - 8.*x.^2 + 1),...
                                      @(x,y,c,AM,PA) (8.*y.^4 - 8.*y.^2 + 1),...
                                      @(x,y,c,AM,PA) (4.*x.^3 - 3.*x).*y,...
                                      @(x,y,c,AM,PA) (4.*y.^3 - 3.*y).*x,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*(2.*y.^2-1)};
                                      
                    FunY        = FunX;
                case 'cheby1_4_c1'
                    % chebyshev polynomials of the first kind, of order 4
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) 2.*x.^2-1,...
                                      @(x,y,c,AM,PA) 2.*y.^2-1,...
                                      @(x,y,c,AM,PA) x.*y,...
                                      @(x,y,c,AM,PA) 4.*x.^3 - 3.*x,...
                                      @(x,y,c,AM,PA) 4.*y.^3 - 3.*y,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*y,...
                                      @(x,y,c,AM,PA) (2.*y.^2-1).*x,...
                                      @(x,y,c,AM,PA) (8.*x.^4 - 8.*x.^2 + 1),...
                                      @(x,y,c,AM,PA) (8.*y.^4 - 8.*y.^2 + 1),...
                                      @(x,y,c,AM,PA) (4.*x.^3 - 3.*x).*y,...
                                      @(x,y,c,AM,PA) (4.*y.^3 - 3.*y).*x,...
                                      @(x,y,c,AM,PA) (2.*x.^2-1).*(2.*y.^2-1),...
                                      @(x,y,c,AM,PA) c,...
                                      @(x,y,c,AM,PA) x.*c,...
                                      @(x,y,c,AM,PA) y.*c};
                                      
                    FunY        = FunX;
                case 'cheby1_2'
                    % chebyshev polynomials of the first kind, of order 3
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) 2.*x.^2-1,...
                                      @(x,y,c,AM,PA) 2.*y.^2-1,...
                                      @(x,y,c,AM,PA) x.*y};
                                      
                    FunY        = FunX;
                case 'poly1'
                    % chebyshev polynomials of the first kind, of order 3
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
