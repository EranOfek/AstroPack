    methods % Constructor
       
        function AstC=Tran2D(varargin)
            % Tran2D class constructor
            % Package: @Tran2D
            % Input  : * Arbitrary number of strings each represents
            %            a transformation functionals (see
            %            Tran2D.selected_trans for options).
            %            Each transformation will populate one element.
            %            Alternatively, this is a number of elements.
            %            Default is 'cheby1_3'.
            % Output : - A Tran2D object
            % Example: TC=Tran2D
            %          TC=Tran2D(2)
            %          TC=Tran2D('cheby1_3_c1','cheby1_2');

            Def.Trans = 'cheby1_3';
            if nargin==0
                varargin{1} = Def.Trans;
            end
            
            if numel(varargin)==1 && isnumeric(varargin{1})
                N = varargin{1};
                varargin = cell(1,N);
                [varargin{1:N}] = deal(Def.Trans);
            end
            
            
            Narg = numel(varargin);
            for Iarg=1:1:Narg
                AstC(Iarg).UserData = [];
                [FunX,FunY,ColCell,PolyX_Xdeg,PolyX_Ydeg,PolyY_Xdeg,PolyY_Ydeg] = Tran2D.selected_trans(varargin{Iarg});
                AstC(Iarg).FunX     = FunX;
                AstC(Iarg).FunY     = FunY;
                AstC(Iarg).ColCell  = ColCell;
                AstC(Iarg).PolyX_Xdeg = PolyX_Xdeg;
                AstC(Iarg).PolyX_Ydeg = PolyX_Ydeg;
                AstC(Iarg).PolyY_Xdeg = PolyY_Xdeg;
                AstC(Iarg).PolyY_Ydeg = PolyY_Ydeg;
            end
                
        end

    end
    
