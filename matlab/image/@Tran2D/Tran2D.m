                    PolyX_Ydeg  = [0 0 1];
                    PolyY_Xdeg  = [0 1 0];
                    PolyY_Ydeg  = [0 0 1];
                case 'poly2'
                    % chebyshev polynomials of the first kind, of order 3
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) x.^2,...
                                      @(x,y,c,AM,PA) y.^2,...
                                      @(x,y,c,AM,PA) x.*y};
                    FunY        = FunX;
                    PolyX_Xdeg  = [0 1 0 2 0 1];
                    PolyX_Ydeg  = [0 0 1 0 2 1];
                    PolyY_Xdeg  = [0 1 0 2 0 1];
                    PolyY_Ydeg  = [0 0 1 0 2 1];
                case 'poly3'
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
                                      @(x,y,c,AM,PA) y,...
                                      @(x,y,c,AM,PA) x.^2,...
                                      @(x,y,c,AM,PA) y.^2,...
                                      @(x,y,c,AM,PA) x.*y,...
                                      @(x,y,c,AM,PA) x.^3,...
                                      @(x,y,c,AM,PA) y.^3,...
                                      @(x,y,c,AM,PA) x.^2.*y,...
                                      @(x,y,c,AM,PA) y.^2.*x};
                    FunY        = FunX;
                    PolyX_Xdeg  = [0 1 0 2 0 1 3 0 2 1];
                    PolyX_Ydeg  = [0 0 1 0 2 1 0 3 1 2];
                    PolyY_Xdeg  = [0 1 0 2 0 1 3 0 2 1];
                    PolyY_Ydeg  = [0 0 1 0 2 1 0 3 1 2];
                case 'poly4'
                    ColCell     = {'x','y','c','AM','PA'};
                    FunX        = {@(x,y,c,AM,PA) ones(size(x)),...
                                      @(x,y,c,AM,PA) x,...
